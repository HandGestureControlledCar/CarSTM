/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */

  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
//	received = 1;
	HAL_UART_Receive(&huart1, myBuff, 1, 100);
	if(received == 0){
		if(myBuff[0] == 'R' )
			direction = 2; 
		else if(myBuff[0] == 'L')
			direction = 4;
		else if(myBuff[0] == 'F')
			direction = 1;
		else if(myBuff[0] == 'B')
			direction = 3;
		else if(myBuff[0] == 'S')
			direction = 5;
		else
			received -=1;
	}else if(received == 1){
		if(myBuff[0] == '0'){
			speed = 0;
		}else if(myBuff[0] == '1'){
			speed = 1;
		}else if(myBuff[0] == '2'){
			speed = 2;
		}else if(myBuff[0] == '3'){
			speed = 3;
		}else if(myBuff[0] == '4'){
			speed = 4;
		}else if(myBuff[0] == '5'){
			speed = 5;
		}else{
			speed = -1;
			received -= 1;
		}
	}else if(received == 2){
		if(myBuff[0] == 'H'){
			horn = 1;
		}else if(myBuff[0] == 'x'){
			horn = 0;
		}
	}
	received += 1;
	if(received > 3) received = 3;

  /* USER CODE END USART1_IRQn 1 */
}

